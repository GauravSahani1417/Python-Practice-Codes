#Python program to print positive numbers in a list

#Input: list1 = [12, -7, 5, 64, -14]
#Output: 12, 5, 64

# list of numbers 
list1 = [11, -21, 0, 45, 66, -93]

# iterating each number in list 
for num in list1: 
      
    # checking condition 
    if num >= 0: 
       print(num, end= " ") 
       
